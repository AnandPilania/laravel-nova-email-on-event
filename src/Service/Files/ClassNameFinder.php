namespace MichielGerritsen\LaravelNovaEmailOnEvent\Service\Files;

use MichielGerritsen\LaravelNovaEmailOnEvent\DataObjects\ClassInfo;

class ClassNameFinder
{
    /**
     * Copied from https://stackoverflow.com/a/7153391/2329962
     *
     * @param \SplFileInfo $file
     * @return ClassInfo
     */
    public function fromFile(\SplFileInfo $file)
    {
        $i = 0;
        $fp = fopen($file->getRealPath(), 'r');
        $class = $namespace = $buffer = '';
        while (!$class) {
            if (feof($fp)) break;

            $buffer .= fread($fp, 512);
            $tokens = token_get_all($buffer);

            if (strpos($buffer, '{') === false) continue;

            for (;$i<count($tokens);$i++) {
                if ($tokens[$i][0] === T_NAMESPACE) {
                    for ($j=$i+1;$j<count($tokens); $j++) {
                        if ($tokens[$j][0] === T_STRING) {
                            $namespace .= '\\'.$tokens[$j][1];
                        } else if ($tokens[$j] === '{' || $tokens[$j] === ';') {
                            break;
                        }
                    }
                }

                if ($tokens[$i][0] === T_CLASS) {
                    for ($j=$i+1;$j<count($tokens);$j++) {
                        if ($tokens[$j] === '{') {
                            $class = $tokens[$i+2][1];
                        }
                    }
                }
            }
        }

        return new ClassInfo($namespace . '\\' . $class, $class);
    }
}