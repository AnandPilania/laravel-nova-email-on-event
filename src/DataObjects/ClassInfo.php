namespace MichielGerritsen\LaravelNovaEmailOnEvent\DataObjects;

class ClassInfo
{
    /**
     * @var string
     */
    private $namespace;

    /**
     * @var string
     */
    private $name;

    public function __construct(
        $namespace,
        $name
    ) {
        $this->namespace = $namespace;
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getNamespace(): string
    {
        return $this->namespace;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}